from ..Architecture import Architecture

########################################################################
class PPC(Architecture):
  """Class for PPC architectures.
  """

  ####################################################################
  # Public methods
  ####################################################################

  ####################################################################
  # Overridden methods
  ####################################################################
  @property
  def is32Bit(self):
    return False

  ####################################################################
  @property
  def is64Bit(self):
    return True

  ####################################################################
  @property
  def isFedoraSecondary(self):
    return True

  ####################################################################
  # Protected methods
  ####################################################################

  ####################################################################
  # Private methods
  ####################################################################

########################################################################
class PPC64(PPC):
  """Class for PPC 64-bit big-endian architectures.
  """
  _available = True

  ####################################################################
  # Overridden methods
  ####################################################################
  @property
  def lacksHardwareData(self):
    return True

  ####################################################################
  @property
  def requiresExternalStorage(self):
    return True

########################################################################
class PPC64LE(PPC):
  """Class for PPC 64-bit little-endian architectures.
  """
  _available = True
